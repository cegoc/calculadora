package com.elizabeth.operationslibrary

class Operations {

    //Funcion Suma
    fun sum(a: Int, b: Int): Int {
        return a + b

    }
    fun subtract(a: Int, b: Int): Int {
        return a - b

    }
    fun multiply(a: Int, b: Int): Int {
        return a * b

    }
    fun divide(a: Double, b: Double): Double {
        return a / b

    }


}